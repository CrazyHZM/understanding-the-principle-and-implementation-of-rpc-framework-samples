package samples.serialization.kryo;

import java.math.BigDecimal;

/**
 * @Description:
 * @author: huazhongming
 * @Date: Created in 2019-09-25 07:40
 */
public class Person {
    private String name;
    private Integer age;

    public Person() {
    }

    public Person(String name, Integer age) {
        this.name = name;
        this.age = age;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Integer getAge() {
        return age;
    }

    public void setAge(Integer age) {
        this.age = age;
    }

}
